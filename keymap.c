bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        // KEYBOARD PET STATUS START
        case KC_LCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;

        case LT(3,KC_SPC):
        case LALT_T(KC_SPC):
            if (record->event.pressed) {
                isJumping = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;

        // KEYBOARD PET STATUS END
    }
    return true;
}


